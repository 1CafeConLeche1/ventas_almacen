Los pingüinos sapaces de retener la mayor parte de su calor corporal y esta adaptación les permite habitar las regiones más frías. Su plumaje consta de tres capas, poseen una capa de grasa bajo la piel, y un sistema de vasos sanguíneos en las aletas y patas especializado para estas regiones expuestas del cuerpo. Estas adaptaciones son tan efectivas en la conservación del calor que las especies que viven en regiones templadas suelen sobrecalentarse, para lo cual poseen sus propias adaptaciones, tales como la existencia de áreas de piel desnuda en el rostro, aletas más largas y patas que actúan como radiadores. Igualmente pasan la mayor parte de su tiempo en el agua fría. Otra adaptación térmica se relaciona con el tamaño, ya que las especies templadas son en general pequeñas (el pingüino de las Galápagos es el más pequeño del género Spheniscus) lo que les ayuda a disipar el calor más velozmente que a las especies de mayor tamaño.[cita requerida]
